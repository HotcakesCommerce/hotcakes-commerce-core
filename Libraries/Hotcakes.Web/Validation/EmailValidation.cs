using System;
using System.Text.RegularExpressions;

namespace Hotcakes.Web.Validation
{
    [Serializable]
    [Obsolete("Obsolete in 1.8.0. Class wasn't used")]
    public class EmailValidation
    {
        public static bool MeetsEmailFormatRequirements(string input)
        {
            var result = false;
            var tester = input.Trim();
            if (tester.Length < 4) return false;

            var formula =
                @"^(?("")("".+?""@)|(([0-9a-zA-Z]((\.(?!\.))|[-!#\$%&'\*\+/=\?\^`\{\}\|~\w])*)(?<=[0-9a-zA-Z])@))(?(\[)(\[(\d{1,3}\.){3}\d{1,3}\])|(([0-9a-zA-Z][-\w]*[0-9a-zA-Z]\.)+[a-zA-Z]{2,6}))$";

            result = Regex.IsMatch(tester, formula, RegexOptions.CultureInvariant);

            return result;
        }
    }
}